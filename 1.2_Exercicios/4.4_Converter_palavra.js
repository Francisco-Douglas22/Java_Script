/*31 - Faça um programa onde converta Uma Palavra em Minúscula para Maiúscula e verse versa */

const palavra = 'Estou Programando em JavaScript';

//Palavra convertida para Minuscula
let minuscula = palavra.toLocaleLowerCase();
console.log(`A Palavra Convertida para minuscula e: ${minuscula}`)

//Palavra convertida para MAIUSCULA
let MAIUSCULA = palavra.toLocaleUpperCase();
console.log(`A Palavra Convertida para MAIUSCULA e: ${MAIUSCULA}`)