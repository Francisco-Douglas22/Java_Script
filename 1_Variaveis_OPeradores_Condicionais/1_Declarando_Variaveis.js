//Formas de declara variaveis em JavaScript

// (let) Com o let consigo altera sempre que necessario a variavel
let variavel_let = 10;

// (const) Com o const a variavel fica imutavel
const variavel_const = 3.4;

// Forma de imprimir a variavel
console.log(variavel_let);
console.log(variavel_const);

// Tentando muda os valores das variaveis

// variavel_const = 22 ira retorna um erro
variavel_let = 12 // mudara facilmente

console.log(`O Valor da variavel sera ${variavel_let}`)