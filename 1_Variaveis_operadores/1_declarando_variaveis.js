// Declarando variáveis em JavaScript

// Utilizando 'let':
// 'let' é uma palavra-chave que declara uma variável local no escopo em que é definida.
// Uma variável declarada com 'let' pode ser reatribuída posteriormente.
let variavel = 'Uma String'; // Variável que pode ser modificada

// Utilizando 'const':
// 'const' é uma palavra-chave que declara uma variável local no escopo em que é definida.
// Uma vez atribuído um valor a uma variável declarada com 'const', esse valor não pode ser alterado.
const constante = 'Uma String'; // Variável que não pode ser modificada

// Utilizando 'var':
// 'var' é uma palavra-chave que declara uma variável local ou global, dependendo de onde é declarada.
// Variáveis declaradas com 'var' podem ser reatribuídas e têm escopo de função.
var var_variavel = 'Outro tipo de variável'; 

// Exibindo variáveis no console:
console.log(var_variavel); // Imprime 'Outro tipo de variável'
console.log(constante); // Imprime 'Uma String'
console.log(variavel); // Imprime 'Uma String'
